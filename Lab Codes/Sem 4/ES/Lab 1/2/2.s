	MOV R0, #0x90000000
	MOV R1, #0x60000000
	MOV R2, #0x70000000
	MOV R3, #0x80000000
	MOV R4, #0xA0000000
	MOV R5, #0xB0000000
	MOV R6, #0xC0000000
	MOV R7, #0xD0000000
	ADDS R12, R3, R7
	ADC R11, R2, R6
	ADC R10, R1, R5
	ADC R9, R0, R4
	ADC R8, R8, #0
	LDR R0, VALUE ;Overwrite R0 with mem loc of VALUE to store (ie @ 0x10000000)
	STR R8, [R0], #4
	STR R9, [R0], #4
	STR R10, [R0], #4
	STR R11, [R0], #4
	STR R12, [R0], #4
STOP
