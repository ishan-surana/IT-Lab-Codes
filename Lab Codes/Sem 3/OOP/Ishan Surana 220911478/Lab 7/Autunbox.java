class Autunbox {
    public static void main(String args[]) {
        Integer i = 5;
        i = i + 10;
        System.out.println(i);

        Long l = 23l;
        l = l + 10;
        System.out.println(l);

        Short s = 23;
        short x = s;
        System.out.println(x);

        Boolean b = true;
        boolean b1 = b;
        System.out.println(b1);

        Double d = 5.3;
        d = d + 10;
        System.out.println(d);

        Float f = 3.4f;
        f = f + 10;
        System.out.println(f);

        Byte by1=10;
        byte by2=by1;
        System.out.println(by2);

        Character c1='A';
        char c2=c1;
        System.out.println(c2);
    }
}